# Math Module Part 2
import math

# Factorial & Square Root
# Calculate the factorial of 3
print(math.factorial(3))  # Output: 6

# Calculate the square root of 64
print(math.sqrt(64))  # Output: 8.0

# Greatest Common Denominator GCD
# Calculate the greatest common divisor of 52 and 8
print(math.gcd(52, 8))  # Output: 4

# Calculate the greatest common divisor of 8 and 52 (order doesn't matter)
print(math.gcd(8, 52))  # Output: 4

# Division examples
print(8/52)  # Output: 0.15384615384615385
print(2/13)  # Output: 0.15384615384615385

# Degrees and Radians
# Convert 360 degrees to radians
print(math.radians(360))  # Output: 6.283185307179586

# Calculate 2 times pi
print(math.pi * 2)  # Output: 6.283185307179586

# Convert 2 times pi radians to degrees
print(math.degrees(math.pi * 2))  # Output: 360.0